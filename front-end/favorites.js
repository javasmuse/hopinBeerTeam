function BeerConstructor(id, name, imgUrl, category, abv,
    type, brewer, country, comments) {
    this.id = id;
    this.name = name;
    this.imgUrl = imgUrl;
    this.category = category;
    this.abv = abv;
    this.type = type;
    this.brewer = brewer;
    this.country = country;
    this.comments = comments;
}